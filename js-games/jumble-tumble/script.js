// ─── STATE ──────────────────────────────────────────────────────────
const ROUNDS        = 10;
const ROUND_TIME    = 30;    // seconds
const HINT_AT       = 10;    // reveal hint when this many seconds remain

let state = {
    difficulty:   null,
    round:        0,
    score:        0,
    correctCount: 0,
    usedIndices:  [],
    currentWord:  '',
    currentHint:  '',
    timeRemaining: ROUND_TIME,
    hintShown:    false,
    timerInterval: null,
    hintTimeout:  null,
    soundOn:      true,
};

// ─── AUDIO ──────────────────────────────────────────────────────────
function playSound(id) {
    if (!state.soundOn) return;
    const el = document.getElementById(id);
    if (!el) return;
    el.currentTime = 0;
    el.play().catch(() => {});
}

function toggleSound() {
    state.soundOn = !state.soundOn;
    document.getElementById('sound-toggle').textContent = state.soundOn ? '🔊' : '🔇';
}

// ─── DIFFICULTY HELPERS ──────────────────────────────────────────────
const DIFF_LABELS = { easy: 'EASY', medium: 'MEDIUM', hard: 'HARD', pro: 'PRO' };

// ─── SHUFFLE ─────────────────────────────────────────────────────────
function shuffleWord(word) {
    const arr = word.split('');
    let shuffled;
    let attempts = 0;
    do {
        // Fisher-Yates
        for (let i = arr.length - 1; i > 0; i--) {
            const j = Math.floor(Math.random() * (i + 1));
            [arr[i], arr[j]] = [arr[j], arr[i]];
        }
        shuffled = arr.join('');
        attempts++;
    } while (shuffled === word && attempts < 20);
    return shuffled;
}

// ─── PICK WORD ───────────────────────────────────────────────────────
function pickWord() {
    const pool = WORDS[state.difficulty];
    const remaining = pool
        .map((_, i) => i)
        .filter(i => !state.usedIndices.includes(i));

    // if we've exhausted the pool reset used (unlikely with large word lists)
    if (remaining.length === 0) {
        state.usedIndices = [];
        return pickWord();
    }

    const idx = remaining[Math.floor(Math.random() * remaining.length)];
    state.usedIndices.push(idx);
    return pool[idx];
}

// ─── RENDER TILES ────────────────────────────────────────────────────
function renderTiles(scrambled) {
    const container = document.getElementById('jumbled-word');
    container.innerHTML = '';
    scrambled.toUpperCase().split('').forEach((ch, i) => {
        const span = document.createElement('span');
        span.className = 'letter-tile';
        span.textContent = ch;
        span.style.animationDelay = `${i * 55}ms`;
        container.appendChild(span);
    });
}

// ─── TIMER ───────────────────────────────────────────────────────────
function startTimer() {
    clearInterval(state.timerInterval);
    state.timeRemaining = ROUND_TIME;

    const bar   = document.getElementById('timer-bar');
    const label = document.getElementById('timer-label');

    bar.style.width = '100%';
    bar.className = '';
    label.textContent = ROUND_TIME;

    state.timerInterval = setInterval(() => {
        state.timeRemaining = Math.max(0, state.timeRemaining - 0.1);
        const pct = (state.timeRemaining / ROUND_TIME) * 100;

        bar.style.width = pct + '%';
        label.textContent = Math.ceil(state.timeRemaining);

        // colour transitions
        bar.classList.toggle('warn', state.timeRemaining <= 20 && state.timeRemaining > 10);
        bar.classList.toggle('crit', state.timeRemaining <= 10);

        // hint reveal
        if (!state.hintShown && state.timeRemaining <= HINT_AT) {
            revealHint();
        }

        if (state.timeRemaining <= 0) {
            clearInterval(state.timerInterval);
            handleTimeout();
        }
    }, 100);
}

function stopTimer() {
    clearInterval(state.timerInterval);
    clearTimeout(state.hintTimeout);
}

// ─── HINT ────────────────────────────────────────────────────────────
function revealHint() {
    state.hintShown = true;
    const area = document.getElementById('hint-area');
    document.getElementById('hint-text').textContent = state.currentHint;
    area.classList.remove('hidden');
}

// ─── SCORING ─────────────────────────────────────────────────────────
function calcPoints() {
    // Full time = 30s, answered instantly (30s remaining) → max pts
    // hintShown halves the multiplier
    const t = state.timeRemaining;
    if (state.hintShown) {
        return Math.max(10, Math.round((t / ROUND_TIME) * 50));
    }
    return Math.max(30, Math.round((t / ROUND_TIME) * 100));
}

// ─── SUBMIT / VALIDATE ───────────────────────────────────────────────
function handleSubmit() {
    const input = document.getElementById('answer-input');
    const guess = input.value.trim().toLowerCase();
    if (!guess) return;

    if (guess === state.currentWord) {
        const pts = calcPoints();
        onCorrect(pts);
    } else {
        onWrong();
    }
}

// ─── CORRECT ─────────────────────────────────────────────────────────
function onCorrect(pts) {
    stopTimer();
    state.score        += pts;
    state.correctCount += 1;

    playSound('snd-correct');

    // tiles flash green
    document.querySelectorAll('.letter-tile').forEach(t => t.classList.add('tile-correct'));

    // input highlight
    const input = document.getElementById('answer-input');
    input.classList.add('input-correct');
    input.disabled = true;

    // feedback pop
    showFeedback(`+${pts}`, true);

    // update HUD score
    document.getElementById('hud-score').textContent = `Score: ${state.score}`;

    setTimeout(() => nextRound(), 900);
}

// ─── WRONG ───────────────────────────────────────────────────────────
function onWrong() {
    stopTimer();
    playSound('snd-incorrect');

    // jiggle tiles
    document.querySelectorAll('.letter-tile').forEach(t => {
        t.classList.remove('tile-jiggle');
        void t.offsetWidth; // reflow to restart animation
        t.classList.add('tile-jiggle');
    });

    const input = document.getElementById('answer-input');
    input.classList.add('input-wrong');
    input.disabled = true;

    // briefly reveal correct word in input
    input.value = state.currentWord;

    showFeedback('✗', false);
    setTimeout(() => nextRound(), 1300);
}

function handleTimeout() {
    onWrong();
}

// ─── FEEDBACK FLASH ──────────────────────────────────────────────────
function showFeedback(text, correct) {
    const el = document.getElementById('feedback');
    el.className = correct ? 'fb-correct' : 'fb-wrong';
    el.textContent = text;
    el.classList.remove('hidden');
    setTimeout(() => el.classList.add('hidden'), 700);
}

// ─── ROUND FLOW ──────────────────────────────────────────────────────
function startRound() {
    state.round++;
    state.hintShown = false;

    // reset UI
    const input = document.getElementById('answer-input');
    input.value = '';
    input.disabled = false;
    input.classList.remove('input-correct', 'input-wrong');
    input.focus();

    document.getElementById('hint-area').classList.add('hidden');
    document.getElementById('feedback').classList.add('hidden');

    // update HUD
    document.getElementById('hud-round').textContent  = `Round ${state.round} / ${ROUNDS}`;
    document.getElementById('hud-score').textContent  = `Score: ${state.score}`;

    // pick + scramble word
    const entry = pickWord();
    state.currentWord = entry.word.toLowerCase();
    state.currentHint = entry.hint;

    const scrambled = shuffleWord(state.currentWord);
    renderTiles(scrambled);

    startTimer();
}

function nextRound() {
    if (state.round < ROUNDS) {
        startRound();
    } else {
        showResult();
    }
}

// ─── GAME START ───────────────────────────────────────────────────────
function startGame(diff) {
    state.difficulty   = diff;
    state.round        = 0;
    state.score        = 0;
    state.correctCount = 0;
    state.usedIndices  = [];

    document.getElementById('hud-level').textContent = DIFF_LABELS[diff];

    // swap screens
    document.getElementById('start-modal').classList.add('hidden');
    document.getElementById('result-modal').classList.add('hidden');
    document.getElementById('leaderboard-modal').classList.add('hidden');
    document.getElementById('game-screen').classList.remove('hidden');

    startRound();
}

// ─── RESULT MODAL ────────────────────────────────────────────────────
function showResult() {
    stopTimer();

    // populate card
    document.getElementById('result-card-level').textContent = DIFF_LABELS[state.difficulty];
    document.getElementById('result-card-game').textContent  = '🔤 Jumble Tumble';

    const won = state.correctCount >= Math.ceil(ROUNDS * 0.6); // 60% correct = win
    document.getElementById('result-icon').textContent  = won ? '🏆' : '🔄';
    document.getElementById('result-title').textContent = won ? 'Well played!' : 'Keep practising!';

    document.getElementById('stat-score').textContent   = state.score;
    document.getElementById('stat-correct').textContent = `${state.correctCount}/${ROUNDS}`;

    const pct = Math.round((state.correctCount / ROUNDS) * 100);
    document.getElementById('stat-pct').textContent = pct + '%';

    // animate pct ring (circumference = 2π×31.5 ≈ 197.9)
    const arc = document.getElementById('pct-arc');
    const CIRC = 197.9;
    setTimeout(() => {
        arc.style.strokeDashoffset = CIRC - (CIRC * pct / 100);
    }, 100);

    // max possible score: 10 × 100 = 1000 (answered at 0s without hint)
    document.getElementById('rst-score-value').textContent = `${state.score} / ${ROUNDS * 100}`;

    // build buttons
    const btns = document.getElementById('result-buttons');
    btns.innerHTML = `
        <button class="btn-share" onclick="shareResult()">📤 SHARE RESULT</button>
        <button class="btn-again" onclick="startGame('${state.difficulty}')">PLAY AGAIN</button>
        <button class="btn-menu"  onclick="resetToStart()">MAIN MENU</button>
    `;

    // save to leaderboard
    saveScore();

    // hide game, show result
    document.getElementById('game-screen').classList.add('hidden');
    document.getElementById('result-modal').classList.remove('hidden');
}

// ─── SHARE RESULT ────────────────────────────────────────────────────
function shareResult() {
    const pct  = Math.round((state.correctCount / ROUNDS) * 100);
    const bar  = buildScoreBar(pct);
    const diff = DIFF_LABELS[state.difficulty];
    const url  = 'https://sharikmp.github.io/js-games/jumble-tumble/';

    const text = [
        `🔤 Jumble Tumble · ${diff}`,
        `Round ${ROUNDS}/${ROUNDS} complete!`,
        `Score: ${state.score}/${ROUNDS * 100}`,
        `Correct: ${state.correctCount}/${ROUNDS}`,
        bar,
        `Play at: ${url}`,
    ].join('\n');

    if (navigator.share) {
        navigator.share({ text }).catch(() => copyToClipboard(text));
    } else {
        copyToClipboard(text);
    }
}

function buildScoreBar(pct) {
    const filled = Math.round(pct / 10);
    return '🟧'.repeat(filled) + '⬛'.repeat(10 - filled);
}

function copyToClipboard(text) {
    if (navigator.clipboard && navigator.clipboard.writeText) {
        navigator.clipboard.writeText(text).then(() => flashShareBtn('COPIED!'));
    } else {
        // fallback
        const ta = document.createElement('textarea');
        ta.value = text;
        ta.style.position = 'fixed';
        ta.style.opacity = '0';
        document.body.appendChild(ta);
        ta.select();
        document.execCommand('copy');
        document.body.removeChild(ta);
        flashShareBtn('COPIED!');
    }
}

function flashShareBtn(msg) {
    const btn = document.querySelector('.btn-share');
    if (!btn) return;
    const orig = btn.textContent;
    btn.textContent = msg;
    setTimeout(() => { btn.textContent = orig; }, 1800);
}

